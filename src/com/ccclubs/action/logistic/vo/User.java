package com.ccclubs.action.logistic.vo;

public class User {
	private String su_id;
	private String su_username;
	private String su_real_name;
	private String su_mobile;
	private String su_email;
	private String su_group;
	private String host;
	private String outlets;
	
	public String getSu_id() {
		return su_id;
	}
	public void setSu_id(String su_id) {
		this.su_id = su_id;
	}
	public String getSu_username() {
		return su_username;
	}
	public void setSu_username(String su_username) {
		this.su_username = su_username;
	}
	public String getSu_real_name() {
		return su_real_name;
	}
	public void setSu_real_name(String su_real_name) {
		this.su_real_name = su_real_name;
	}
	public String getSu_mobile() {
		return su_mobile;
	}
	public void setSu_mobile(String su_mobile) {
		this.su_mobile = su_mobile;
	}
	public String getSu_email() {
		return su_email;
	}
	public void setSu_email(String su_email) {
		this.su_email = su_email;
	}
	public String getSu_group() {
		return su_group;
	}
	public void setSu_group(String su_group) {
		this.su_group = su_group;
	}
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getOutlets() {
		return outlets;
	}
	public void setOutlets(String outlets) {
		this.outlets = outlets;
	}
}
